class  HotDog:
    def __init__(self):
        self.cooked_level=0
        self.cooked_string="Siraya"
        self.condiments=[]
    def __str__(self):
        msg="sosiska"
        if len(self.condiments)>0:
            msg=msg+'c'
        for i in self.condiments:
            msg=msg+i+", "
        msg=msg.strip(", ")
        msg=self.cooked_string+" "+ msg+"."
        return msg
    def cook(self,time):
        self.cooked_level=self.cooked_level+time
        if self.cooked_level>8:
            self.cooked_string="Sgorevshaya"
        elif self.cooked_level>5:
            self.cooked_string="Horosho progarenaya"
        elif self.cooked_level>3:
            self.cooked_string="Srednei progarki"
        else:
            self.cooked_string="Siraya"
    def addCondiment(self, condiment):
        self.condiments.append(condiment)
